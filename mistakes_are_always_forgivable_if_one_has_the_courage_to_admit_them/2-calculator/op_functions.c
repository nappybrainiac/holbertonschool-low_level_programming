/*These small functions perform simple
  mathematical equations.*/

/*Addition*/
int op_add(int a, int b){
  return a + b;
}

/*Subtraction*/
int op_sub(int a, int b){
  return a - b;
}

/*Multiplication*/
int op_mul(int a, int b){
  return a * b;
}

/*Division*/
int op_div(int a, int b){
  return a / b;
}

/*Modulus*/
int op_mod(int a, int b){
  return a % b;
}
